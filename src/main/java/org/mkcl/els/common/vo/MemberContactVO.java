package org.mkcl.els.common.vo;

public class MemberContactVO {

	private String fullName;
	
	private String mobile;
	
	private String email;	
	
	private String permanentTelephone;
	
	private String presentTelephone;
	
	private String officeTelephone;
	
	private String mumbaiTelephone;
	
	private String nagpurTelephone;

	public String getFullName() {
		return fullName;
	}

	public void setFullName(String fullName) {
		this.fullName = fullName;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getPermanentTelephone() {
		return permanentTelephone;
	}

	public void setPermanentTelephone(String permanentTelephone) {
		this.permanentTelephone = permanentTelephone;
	}

	public String getPresentTelephone() {
		return presentTelephone;
	}

	public void setPresentTelephone(String presentTelephone) {
		this.presentTelephone = presentTelephone;
	}

	public String getOfficeTelephone() {
		return officeTelephone;
	}

	public void setOfficeTelephone(String officeTelephone) {
		this.officeTelephone = officeTelephone;
	}

	public String getMumbaiTelephone() {
		return mumbaiTelephone;
	}

	public void setMumbaiTelephone(String mumbaiTelephone) {
		this.mumbaiTelephone = mumbaiTelephone;
	}

	public String getNagpurTelephone() {
		return nagpurTelephone;
	}

	public void setNagpurTelephone(String nagpurTelephone) {
		this.nagpurTelephone = nagpurTelephone;
	}
	
	
	
}
