package org.mkcl.els.common.vo;

public class MemberBallotFinalBallotQuestionVO {

	private String questionId;
	
	private String questionNumber;
	
	private String questionPosition;
	
	private String questionRound;
	
	private String questionAttendance;

	public String getQuestionId() {
		return questionId;
	}

	public void setQuestionId(String questionId) {
		this.questionId = questionId;
	}

	public String getQuestionNumber() {
		return questionNumber;
	}

	public void setQuestionNumber(String questionNumber) {
		this.questionNumber = questionNumber;
	}

	public String getQuestionPosition() {
		return questionPosition;
	}

	public void setQuestionPosition(String questionPosition) {
		this.questionPosition = questionPosition;
	}

	public String getQuestionRound() {
		return questionRound;
	}

	public void setQuestionRound(String questionRound) {
		this.questionRound = questionRound;
	}

	public String getQuestionAttendance() {
		return questionAttendance;
	}

	public void setQuestionAttendance(String questionAttendance) {
		this.questionAttendance = questionAttendance;
	}	
}
