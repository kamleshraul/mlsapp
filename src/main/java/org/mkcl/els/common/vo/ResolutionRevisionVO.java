package org.mkcl.els.common.vo;

public class ResolutionRevisionVO {
	private String editedAs;

	/** The edited by. */
	private String editedBY;

	/** The edited on. */
	private String editedOn;

	/** The status. */
	private String status;

	/** The subject. */
	private String subject;

	/** The question. */
	private String resolution;

	/** The remarks. */
	private String remarks;
	
	/** The discussion date. */
	private String discussionDate;

	public String getEditedAs() {
		return editedAs;
	}

	public void setEditedAs(final String editedAs) {
		this.editedAs = editedAs;
	}

	public String getEditedBY() {
		return editedBY;
	}

	public void setEditedBY(final String editedBY) {
		this.editedBY = editedBY;
	}

	public String getEditedOn() {
		return editedOn;
	}

	public void setEditedOn(final String editedOn) {
		this.editedOn = editedOn;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(final String status) {
		this.status = status;
	}

	public String getSubject() {
		return subject;
	}

	public void setSubject(final String subject) {
		this.subject = subject;
	}

	public String getResolution() {
		return resolution;
	}

	public void setResolution(final String resolution) {
		this.resolution = resolution;
	}

	public String getRemarks() {
		return remarks;
	}

	public void setRemarks(final String remarks) {
		this.remarks = remarks;
	}

	public String getDiscussionDate() {
		return discussionDate;
	}

	public void setDiscussionDate(final String discussionDate) {
		this.discussionDate = discussionDate;
	}
	
	
}
