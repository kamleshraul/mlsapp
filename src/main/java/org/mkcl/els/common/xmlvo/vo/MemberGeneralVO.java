package org.mkcl.els.common.vo;

/**
 * The Class MemberGeneralVO.
 *
 * @author amitd
 * @author sandeeps
 * @since v1.0.0
 */
public class MemberGeneralVO {

/** The full name. */
private String fullName;

/** The constituency no. */
private String constituencyNo;

/** The constituency name. */
private String constituencyName;

/** The constituency district. */
private String constituencyDistrict;

/** The constituency reservation. */
private String constituencyReservation;

/** The party name. */
private String partyName;

/**
 * Gets the full name.
 *
 * @return the full name
 */
public String getFullName() {
    return fullName;
}

/**
 * Sets the full name.
 *
 * @param fullName the new full name
 */
public void setFullName(final String fullName) {
    this.fullName = fullName;
}

/**
 * Gets the constituency no.
 *
 * @return the constituency no
 */
public String getConstituencyNo() {
    return constituencyNo;
}

/**
 * Sets the constituency no.
 *
 * @param constituencyNo the new constituency no
 */
public void setConstituencyNo(final String constituencyNo) {
    this.constituencyNo = constituencyNo;
}

/**
 * Gets the constituency name.
 *
 * @return the constituency name
 */
public String getConstituencyName() {
    return constituencyName;
}

/**
 * Sets the constituency name.
 *
 * @param constituencyName the new constituency name
 */
public void setConstituencyName(final String constituencyName) {
    this.constituencyName = constituencyName;
}

/**
 * Gets the constituency district.
 *
 * @return the constituency district
 */
public String getConstituencyDistrict() {
    return constituencyDistrict;
}

/**
 * Sets the constituency district.
 *
 * @param constituencyDistrict the new constituency district
 */
public void setConstituencyDistrict(final String constituencyDistrict) {
    this.constituencyDistrict = constituencyDistrict;
}

/**
 * Gets the constituency reservation.
 *
 * @return the constituency reservation
 */
public String getConstituencyReservation() {
    return constituencyReservation;
}

/**
 * Sets the constituency reservation.
 *
 * @param constituencyReservation the new constituency reservation
 */
public void setConstituencyReservation(final String constituencyReservation) {
    this.constituencyReservation = constituencyReservation;
}

/**
 * Gets the party name.
 *
 * @return the party name
 */
public String getPartyName() {
    return partyName;
}

/**
 * Sets the party name.
 *
 * @param partyName the new party name
 */
public void setPartyName(final String partyName) {
    this.partyName = partyName;
}
}
